drop table if exists lists;
create table lists(
  id integer primary key,
  category_id integer,
  user_id integer,
  title text,
  price integer,
  spent_date text
);

drop table if exists categories;
create table categories(
  id integer primary key,
  name text
);

drop table if exists users;
create table users(
  id integer primary key,
  user_name text,
  user_password text
);
